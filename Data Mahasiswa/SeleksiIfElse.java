class SeleksiIfElse {
public static void main(String[] args) {
int x;
x = 10;
if(x>5) {
System.out.println("Pernyataan 1a Test Seleksi If dieksekusi");
System.out.println("Pernyataan 1b Test Seleksi If dieksekusi");
}
else {
System.out.println("Pernyataan 2a Test Seleksi Else dieksekusi");
System.out.println("Pernyataan 2b Test Seleksi Else dieksekusi");
}
}
}