//Latihan 6c. Program if dalam if else
class SeleksiIfElseIf{
public static void main(String[] args) {
int bulan = 12;
if(bulan<=3)
System.out.println("Kuartal 1 Test Seleksi If 1");
else if(bulan<=6)
System.out.println("Kuartal 2 Test Seleksi else 1 If 2");
else if(bulan<=9)
System.out.println("Kuartal 3 Test Seleksi else 2 If 3");
else
System.out.println("Kuartal 4 Test Seleksi else 3 If 4");
}
}